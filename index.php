			function enviarEmail(){
			
			var name = $('#name').val();
			var email = $('#email').val();
			var phone = $('#phone').val();
			var message = $('#message').val();
			$("#name").focus();
			if(name ==null || name.trim() ==''){
			$("#success > p").remove();
			$("#success").append("<p class='alert alert-danger'>* Preencha nome</p>");
			$("#name").focus();
			return;
			} if(email ==null || email.trim() ==''){
			$("#success > p").remove();
			$("#success").append("<p class='alert alert-danger'>* Preencha email</p>");
			$("#email").focus();
			return;
			} if(phone ==null || phone.trim() == ''){
			$("#success > p").remove();
			$("#success").append("<p class='alert alert-danger'>* Preencha telefone</p>");
			$("#phone").focus();
			return;
			} 
			if(message ==null || message.trim() == ''){
			$("#success > p").remove();
			$("#success").append("<p class='alert alert-danger'>* Preencha Mensagem</p>");
			$("#message").focus();
			return;
			}		
			var xmlhttp=new XMLHttpRequest();
			xmlhttp.onreadystatechange=function() {
			if (this.readyState==4 && this.status==200) {
			document.getElementById("success").innerHTML=this.responseText;
			document.getElementById("name").value="";
			document.getElementById("email").value="";
			document.getElementById("phone").value="";
			document.getElementById("message").value="";
			}
			}
			xmlhttp.open("GET","enviar_email.php?email="+email+"&name="+name+"&phone="+phone+"&message="+message,true);
			xmlhttp.send();
			}	
			</script>
